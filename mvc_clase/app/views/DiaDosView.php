    <h3>Construyendo un CRUD con Laravel y Filament <i class="fa fa-computer"></i></h3>
    <div class="w-100 d-flex justify-content-center mb-3">
        <img src="/mvc_clase/public/images/dia2/ponencia2_p1.png" alt="colegas" class="img-thumbnail w-50">
    </div>
    <p>La segunda ponencia estuvo a cargo del <b>Ingeniero Gabriel Castillo</b> llevada a cabo a las <b>10:00AM</b>, quien abordó el tema <b>“Creando un CRUD con Laravel y Filament”</b>, mostrando cómo desarrollar un CRUD (Create, Read, Update, Delete) para gestionar información de una empresa de coches y reparaciones.</p>
    <p>Durante la demostración, los estudiantes aprendieron a instalar Laravel, uno de los frameworks más populares de PHP, utilizando Composer, una herramienta que permite gestionar dependencias y paquetes dentro de un proyecto.</p>
    <p>Los pasos básicos que explicó fueron:</p>
    <ul>
        <li>Instalar Composer, el gestor de dependencias de PHP.</li>
        <li>Crear un nuevo proyecto de Laravel.</li>
        <li>Configurar la base de datos en el archivo .env (en este caso, con MySQL).</li>
        <li>Ejecutar el servidor local</li>
    </ul>

    <p>El <b>Ingeniero Castillo</b> también mostró cómo utilizar <b>Laragon</b> para gestionar entornos de desarrollo de manera rápida y sencilla, aunque dejó libertad a los estudiantes de elegir entre <b>Docker, Laragon o XAMPP</b> según sus preferencias.</p>
    <p>Luego introdujo <b>Filament</b>, un poderoso panel administrativo que se integra con Laravel para crear interfaces visuales profesionales sin escribir grandes cantidades de código frontend. Con Filament, es posible generar CRUDs, manejar roles de usuario, crear formularios y paneles administrativos en minutos, agilizando el desarrollo y facilitando la gestión de datos dentro de las aplicaciones.</p>
