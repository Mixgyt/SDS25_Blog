    <h3>IA Multimodal — creando un futuro visual e inteligente con n8n <i class="fa fa-brain"></i></h3>
    <p>La última ponencia del día estuvo a cargo del <b>Ingeniero José Salmerón</b> a las <b>3:00PM</b>, quien abordó uno de los temas más fascinantes y actuales de la tecnología moderna: la Inteligencia Artificial Multimodal.</p>
    <p>Bajo el título <i>“IA Multimodal: creando un futuro visual con n8n — de texto a imagen, conectan y transforman”</i>, los asistentes exploraron cómo la inteligencia artificial puede integrar texto, imagen, audio y video para crear sistemas más inteligentes y cercanos al modo en que las personas perciben el mundo.</p>
    <div class="w-100 d-flex justify-content-center mb-3">
        <img src="/mvc_clase/public/images/dia2/ponencia3_p3.png" alt="colegas" class="img-thumbnail w-50">
    </div>
    <p>Durante la exposición, el Ingeniero Salmerón presentó los avances más recientes en modelos de inteligencia artificial como <b>GPT-5, Gemini y Claude 3</b>, destacando su capacidad para comprender diferentes tipos de información de forma simultánea y generar respuestas coherentes en múltiples formatos. Estas tecnologías ya están transformando áreas como el diseño gráfico, la educación y la salud, donde la IA multimodal permite optimizar procesos y ofrecer experiencias más personalizadas.</p>
    <p>También se profundizó en las IA generadoras de imágenes, como DALL·E, y en el concepto de ingeniería de prompts, una práctica que consiste en formular instrucciones claras y estratégicas para obtener resultados precisos de los modelos de IA. Este tipo de ingeniería se ha convertido en una habilidad clave para quienes desean aprovechar al máximo las herramientas de inteligencia artificial generativa.</p>
    <p>Para cerrar la ponencia, el Ingeniero Salmerón realizó una demostración práctica utilizando n8n, una plataforma de automatización visual sin código. Mostró cómo crear flujos de trabajo inteligentes conectando diferentes servicios, entre ellos un bot de Telegram, capaz de recibir mensajes y responder automáticamente.</p>
    <p>Esta integración permitió ejemplificar cómo la IA multimodal y las herramientas de automatización pueden trabajar juntas para desarrollar soluciones innovadoras en distintos sectores.</p>
