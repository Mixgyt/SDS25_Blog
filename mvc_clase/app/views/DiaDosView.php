    <h3>Toma de decisiones estratégicas basadas en datos <i class="fa fa-puzzle-piece"></i></h3>
    
    <p>La jornada inició a las <b>8:00AM</b> con una interesante ponencia sobre Toma de decisiones estratégicas basadas en datos, dirigida por el <b>Ingeniero Enrique Cortez</b>. En ella, los estudiantes aprendieron cómo transformar los datos en información útil para guiar las decisiones empresariales y estratégicas.</p>
    <p>El ponente explicó conceptos clave para entender el valor del análisis de datos:</p>
    
    <p><b>¿Qué es un dato?</b> <i class="fa fa-puzzle-piece"></i></p>
    <p>Un dato es una unidad básica de información, un hecho o valor sin interpretar, como el numero 9 por si solo. Por sí solo, un dato no aporta conocimiento, pero es el punto de partida del análisis.</p>
    
    <p><b>¿Qué es la información?</b> <i class="fa fa-book"></i></p>
    <p>Cuando los datos se organizan, interpretan y contextualizan, se convierten en información. Es decir, la información surge al darle sentido a los datos. Por ejemplo, saber que “los alumnos obtuvieron 9 en su examen” es información obtenida tras analizar varios datos individuales.</p>
    <div class="w-100 d-flex justify-content-center mb-3">
        <img src="/mvc_clase/public/images/dia2/ponencia1_datos.jpeg" alt="colegas" class="img-thumbnail w-50">
    </div>
    <p><b>¿Qué es la información estructurada?</b> <i class="fa fa-chess-rook"></i></p>
    <p>La información estructurada se presenta en un formato definido y ordenado, como tablas o bases de datos, lo que facilita su análisis con herramientas tecnológicas. Gracias a esta estructura, los ingenieros pueden procesar, filtrar y visualizar datos de forma eficiente.</p>
    
    <p>Durante la ponencia, el ponente trabajo con <b>Microsoft Excel</b>, utilizando tablas dinámicas para filtrar, organizar y presentar datos de manera visualmente atractiva. Posteriormente, se mostró cómo complementar este análisis mediante herramientas de Python, aplicando código para procesar datos y automatizar cálculos.
    El <b>Ingeniero Cortez</b> destacó que, aunque los usuarios no técnicos suelen preferir visualizar resultados en Excel o gráficos, los ingenieros de sistemas tienen el rol de diseñar el código y los procesos que convierten los datos en información útil, presentándola de una forma <b>amigable y comprensible</b> para todos.</p>
