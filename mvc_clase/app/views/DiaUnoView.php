<div class="border p-3 rounded-2 mb-3">
    <h3>Inauguración de la SDS25 <i class="fa-solid fa-flag-checkered"></i></h3>
    <p>Este 13 de octubre de 2025 se dio inicio a una de las celebraciones más esperadas por los estudiantes de <b>Ingeniería de Sistemas Informáticos</b> de la <b>Universidad de El Salvador (UES)</b>: la <b>Semana de Sistemas</b>.</p>
    <p>El evento inaugural comenzó con la presentación de los miembros de la mesa de honor, seguido por la entonación solemne del Himno Nacional de El Salvador. Posteriormente, cada integrante de la mesa dirigió palabras de motivación a los futuros ingenieros, destacando la importancia de la innovación, la constancia y el compromiso académico que caracteriza a la carrera.</p>
</div>

