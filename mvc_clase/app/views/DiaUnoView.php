<div class="border p-3 rounded-2 mb-3">
    <h3>Hackaton <i class="fa-solid fa-laptop-code"></i></h3>
    <p>Durante la tarde, se llevó a cabo una hackatón que reunió a cinco equipos de estudiantes, quienes presentaron proyectos diseñados para solventar problemáticas sociales mediante soluciones tecnológicas.</p>
    <p>Entre las propuestas destacaron:</p>
    <ul>
        <li>Un sistema para organizar eventos universitarios.</li>
        <li>Un registro digital para una veterinaria.</li>
        <li>Un sistema de asistencia para alumnos con PHP.</li>
        <li>Una aplicación de asistencia en terminal.</li>
        <li>Un juego interactivo de bingo desarrollado en Node.js para centros sociales.</li>
    </ul>
    <p>Tras una jornada llena de creatividad y esfuerzo, el <b>equipo del juego de bingo</b> resultó ganador de la hackatón, destacándose por su originalidad y aplicación práctica.</p>
